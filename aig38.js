var city_country = function (city, country) {
    return "".concat(city, ", ").concat(country);
};
// Calling the function with city-country pairs
var location1 = city_country("Lahore", "Pakistan");
var location2 = city_country("London", "United Kingdom");
var location3 = city_country("Sydney", "Australia");
console.log(location1);
console.log(location2);
console.log(location3);
